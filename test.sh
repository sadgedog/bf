#!bin/bash

test() {
    expected="$1"
    input="$2"
    actual="$(./main "$2")"

    if [ "$actual" = "$expected" ]; then
        echo ""
        echo "$input => $actual"
    else
        echo ""
        echo "$input => $expected expected, but got $actual"
        exit 1
    fi
